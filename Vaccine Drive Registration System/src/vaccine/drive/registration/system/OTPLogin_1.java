    private void jLabel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3MouseClicked
        // TODO add your handling code here:
        if(jTextField1.getText().length()==10)
        {
            int vid=validatevaccinator(jTextField1.getText());
            if(jCheckBox1.isSelected() && vid  == -1)
            {
                JOptionPane.showMessageDialog(this,"Enter valid Vaccinator login Number");
                return;
            }
            if(jCheckBox1.isSelected())
            {
                VaccineDriveRegistrationSystem.vaccinator_login=vid;
            }
            
            OTP.generateOTP();
            String message="Your OTP is "+OTP.otp+"\n"+new Date().toLocaleString();
            String number=jTextField1.getText();
            
            int rcode = OTP.sendSms(message, number);
            System.out.println("**"+OTP.otp);
            
            if(rcode==200)
            {
                VaccineDriveRegistrationSystem.currentLogin = jTextField1.getText();
                new OTPLogin_2().setVisible(true);
                this.setVisible(false);
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Invalid Mobile Number!...Please Renter Mobile Number");
            }
            
        }
    }//GEN-LAST:event_jLabel3MouseClicked

