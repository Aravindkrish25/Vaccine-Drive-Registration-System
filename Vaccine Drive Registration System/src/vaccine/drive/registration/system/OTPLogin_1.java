    public static int validatevaccinator(String phone)
    {
        int vid=-1;
        try 
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection connection = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/VDRS", "root", "Bh@280801");


            String command1 = "select * from vaccinator where v_phone = ?";
            PreparedStatement st = connection.prepareStatement(command1);
            st.setString(1, phone);
            ResultSet result = st.executeQuery();
            vid=-1;
            
            if(result.next()) 
            {
               vid = result.getInt("v_id");
            }
            return vid;
        }
        catch(Exception e)
        {
            System.out.print(e);
        }
        return vid;
    }

