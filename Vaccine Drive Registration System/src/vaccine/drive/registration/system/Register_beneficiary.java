    private void jComboBox2ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox2ItemStateChanged
        // TODO add your handling code here:
        ImageIcon image = new ImageIcon("aadhar_number.png");
        if(jComboBox2.getSelectedIndex()==0)
        {
            image = new ImageIcon(getClass().getResource("aadhar_number.png"));
        }
        else if(jComboBox2.getSelectedIndex()==1)
        {
            image = new ImageIcon(getClass().getResource("pan_card_number.png"));
        }
        else if(jComboBox2.getSelectedIndex()==2)
        {
            image = new ImageIcon(getClass().getResource("voter_id.png"));
        }
        else if(jComboBox2.getSelectedIndex()==3)
        {
            image = new ImageIcon(getClass().getResource("driving_license.png"));
        }
        jLabel12.setIcon(image);
    }//GEN-LAST:event_jComboBox2ItemStateChanged

