    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        jLabel18.setVisible(false);
        jLabel19.setVisible(false);
        jLabel20.setVisible(false);
        
        boolean flag1=true;
        int ch = jComboBox2.getSelectedIndex();
        boolean flag = RegisterBeneficiary.validate_proof(ch+1, jTextField4.getText());
        if(!flag)
        {
            jLabel18.setVisible(true);
            flag1=false;
        }
        flag = RegisterBeneficiary.validate_name(jTextField5.getText());
        if(!flag)
        {
            jLabel19.setVisible(true);
            flag1=false;
        }
        flag = RegisterBeneficiary.validate_age(Integer.parseInt(jTextField6.getText()));
        if(!flag)
        {
            jLabel20.setVisible(true);
            flag1=false;
        }
        if(RegisterBeneficiary.noofbeneficiaries(VaccineDriveRegistrationSystem.currentLogin)>=4)
        {
                flag1=false;
                JOptionPane.showMessageDialog(this,"Exceeded number of beneficiaries(>4) for the mobile number");
        }
        if(flag1)
        {
            String g ="M";
            if(jRadioButton4.isSelected())
            {
                g="F";
            }
            
            String name = jTextField5.getText();
            int age = Integer.parseInt(jTextField6.getText());
            String phone = VaccineDriveRegistrationSystem.currentLogin;
            String proofid = jTextField4.getText();
            RegisterBeneficiary.insert(name,age,phone,ch+1,proofid,g);
            JOptionPane.showMessageDialog(this,"Register Successful");
            new home_page().setVisible(true);
            this.setVisible(false);
        }
        

    }//GEN-LAST:event_jButton1ActionPerformed

